# Create a map of tzs for a country code
COUNTRY_TZ_MAP = {}
for prefix in PHONE_TZ_MAP:
    country_codes = PHONE_TZ_MAP[prefix]['country_code'].split()
    for country_code in country_codes:
        if country_code in COUNTRY_TZ_MAP:
            COUNTRY_TZ_MAP[country_code]['tzs'].extend([tz for tz in PHONE_TZ_MAP[prefix]['tzs'] if tz not in COUNTRY_TZ_MAP[country_code]['tzs']])
        else:
            COUNTRY_TZ_MAP[country_code] = {
                'country': PHONE_TZ_MAP[prefix]['country'],
                'tzs': PHONE_TZ_MAP[prefix]['tzs']
            }

# Create a map of tzs for an offset
OFFSET_TZ_MAP = {}
for prefix in PHONE_TZ_MAP:
    for tz in PHONE_TZ_MAP[prefix]['tzs']:
        if tz['offset'] in OFFSET_TZ_MAP:
            if {'code': tz['code'],'label': tz['label']} not in OFFSET_TZ_MAP[tz['offset']]:
                OFFSET_TZ_MAP[tz['offset']].append({
                    'code': tz['code'],
                    'label': tz['label']
                })
        else:
            OFFSET_TZ_MAP[tz['offset']] = [
                {
                    'code': tz['code'],
                    'label': tz['label']
                }
            ]

